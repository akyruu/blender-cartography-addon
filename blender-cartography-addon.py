class CartographyReaderMode(Enum):
    HEADER = 1
    ROOM = 2
    POINT = 3
    COMMENT = 4

class CartographyReader:
    # Fields
    __separator = '\t'
    __pointTypeMap = {
        '(Outline|Contour)': CartographyPointCategory.OUTLINE,
        '(Gate|Porte|EntrÃ©e) ([0-9]+)': CartographyPointCategory.GATE,
        '(Escarpment|Escarpement) ([0-9]+)': CartographyPointCategory.ESCARPMENT,
        '(Column|Colonne) ([0-9]+)': CartographyPointCategory.COLUMN,
        '(Chasm|Gouffre) ([0-9]+)': CartographyPointCategory.CHASM,
        '(Climbing ?Point|Point( d[\'â€™ ]?)?escalade)': CartographyPointCategory.CLIMBING_POINT,
        '(Harvestables?|Consommables?)': CartographyPointCategory.HARVESTABLE,
        '((Anthropogenics? )?Objects?|Objets?( Anthropiques?))': CartographyPointCategory.ANTHROPOGENIC_OBJECT
    }
    __itemTypeMap = {
        '(Box(es)?|Caisses?)': CartographyItemType.BOX,
        'Lichens?': CartographyItemType.LICHEN,
        '(Ores?|Minerai?)': CartographyItemType.ORE
    }

    __row = 0
    __column = 0
    __mode = CartographyReaderMode.HEADER

    __rooms = {}
    __pointNames = []
    __room: CartographyRoom = None
    __pointType: CartographyPointCategory = None
    
    __logger = Logger('CartographyReader')

    # Constructors
    def __init__(self, filepath):
        self.filepath = filepath
        self.__switcher = {
            CartographyReaderMode.HEADER: self.__readHeader,
            CartographyReaderMode.ROOM: self.__readRoom,
            CartographyReaderMode.POINT: self.__readPoint,
            CartographyReaderMode.COMMENT: self.__readComment
        }
    
    # Methods - Reading
    def read(self):
        with open(self.filepath, 'r', encoding='utf8') as file:
            for line in file:
                self.__row += 1
                line = line.strip()
                if not line:
                    self.__mode = CartographyReaderMode.COMMENT
                    continue

                func = self.__switcher.get(self.__mode, None)
                if func == None:
                    raise ValueError('Invalid mode <' + self.__mode.name + '>')
                func(line)
        return self.__rooms
    
    def __readHeader(self, line: str):
        self.__checkLine(line, 'header', ['X', 'Y', 'Z', 'Observation.*'])
        self.__logger.trace('Header found!')
        self.__mode = CartographyReaderMode.ROOM

    def __readRoom(self, line: str):
        matches = self.__checkLine(line, 'room', ['((Room|Salle) [0-9]+) ([A-Za-z].+)'])
        m = matches[0]
        self.__logger.trace('Room line found #' + str(self.__row))
        
        # Get or create room
        name = m.group(1)
        room = self.__rooms.get(name, None);
        if room == None:
            self.__logger.debug('Create new room <' + name + '>')
            room = CartographyRoom(name)
            self.__rooms[name] = room
        else:
            self.__logger.debug('Use existing room <' + name + '>')
        self.__room = room

        # Get default point type for current room
        self.__column = 1
        self.__pointType = self.__checkPointType(m.group(3))
        
        self.__mode =  CartographyReaderMode.POINT

    def __readPoint(self, line: str):
        patterns = ['-?[0-9]+', '-?[0-9]+', '(-?[0-9]+)?', '([A-Za-z].+)?']
        matches = self.__checkLine(line, 'point', patterns, False)
        if matches == None:
            try:
                self.__readRoom(line)
            except:
                self.__raiseError(line, 'point or room', '|'.join(self.__pointTypeMap.keys()) \
                        + ' or ' + '|'.join(patterns))
        elif self.__room is not None: # Always true in prod runtime
            self.__logger.trace('Point line found: #' + str(self.__row))
            matchesCount = len(matches)
            
            # Determine coordinates
            x = int(matches[0].group(0))
            y = int(matches[1].group(0))
            z = int(matches[2].group(0)) if matchesCount > 2 and matches[2].group(0) else 0
            
            # Determine name, point type and sub type
            pointType = self.__pointType
            itemType = None
            if matchesCount > 3:
                name = matches[3].group(0)
                itemType = self.__checkSubType(name, itemType)
                if itemType == None:
                    pointType = self.__checkPointType(name, pointType)
            elif pointType == None: # Always false in prod runtime
                raise Exception('Point line found but not the point type: #' + str(self.__row))
            else:
                name = self.__room.name + '_' + pointType.name
            
            # Check name is unique
            if name in self.__pointNames:
                uniqueName = name
                idx = 1
                while uniqueName in self.__pointNames:
                    uniqueName = '{}_{:03d}'.format(name, idx)
                    idx += 1
                name = uniqueName
            self.__pointNames.append(name)

            # Create and add point to current room
            self.__logger.debug('Create new point <name=' + name + ', type=' + pointType.name \
                    + itemType.name if itemType != None else '' \
                    + ', x=' + str(x) + ', y=' + str(y) + ', z=' + str(z) + '> for room <' + self.__room.name + '>')
            point = CartographyPoint(name, pointType, x, y, z, itemType)
            self.__room.points.append(point)
        else:
            raise Exception('Point line found but no room found')

    def __readComment(self, line: str):
        self.__ignoreLine(line)
    
    # Methods - Tools
    def __checkLine(self, line: str, type: str, patterns: list, strict = True):
        data = line.split(self.__separator)
        matches = []
        columns = len([pattern for pattern in patterns if re.match('\\(.+\\)\\?', pattern)])
        count = max(len(data), columns)
        
        # Check number of columns
        if len(data) < count:
            if strict:
                self.__raiseError(line, type, self.__separator.join(patterns))
            return None
        
        # Check data
        for i in range(count):
            self.__column = i + 1
            pattern = patterns[i]
            value = data[i]
            m = re.match(pattern, value, re.IGNORECASE)
            if m == None:
                if strict:
                    self.__raiseError(value, type, pattern)
                return None
            matches.append(m)
        
        return matches

    def __checkPointType(self, value: str, defaultValue = None):
        for pattern in self.__pointTypeMap:
            if re.match(pattern, value, re.IGNORECASE) != None \
                    or re.match(pattern + '.*', value, re.IGNORECASE) != None \
                    or re.match('.*' + pattern + '.*', value, re.IGNORECASE) != None:
                return self.__pointTypeMap[pattern]
        if defaultValue == None:
            self.__raiseError(value, 'point type', '|'.join(self.__pointTypeMap.keys()))
        return defaultValue

    def __checkSubType(self, value: str, defaultValue = None):
        for pattern in self.__itemTypeMap:
            if re.match(pattern, value, re.IGNORECASE) != None \
                    or re.match(pattern + '.*', value, re.IGNORECASE) != None \
                    or re.match('.*' + pattern + '.*', value, re.IGNORECASE) != None:
                return self.__itemTypeMap[pattern]
        return defaultValue

    def __formatValue(self, line: str):
        return line.replace('\n', '\\n') \
                .replace('\t', '\\t')

    def __ignoreLine(self, line: str):
        self.__logger.info('Ignore <' + self.__formatValue(line) + '> (l.' + str(self.__row) + ')')

    def __raiseError(self, value: str, type: str, pattern: str):
        raise Exception('Invalid ' + type + ':' + ' <' + self.__formatValue(value) + '>' \
                    + ' (l.' + str(self.__row) + ', c.' + str(self.__column) + ').' \
                + ' Expected: <' + pattern + '>' \
                    + ' (case insensitive)')

